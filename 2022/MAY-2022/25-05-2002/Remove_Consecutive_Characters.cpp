class Solution{
    public:
    string removeConsecutiveCharacter(string S)
    {
        // code here.
        string ans="";
        for(int i=0;i<S.length();i++){
            ans+=S[i];
            while(S[i]==S[i+1]){
                i++;
            }
        }
        return ans;
    }
};

